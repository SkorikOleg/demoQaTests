package guru.qa;

public class Main { // класс main
    //написание кода всегда начинается с метода main
    public static void main(String[] args) {

        String statement = "Hello World!";
        System.out.println(statement);

        // операторы:
        // 1. Арифметические - ипользуются для целочис. и плав. точкой + - * / % (взять остаток от деления)
        // ++ (инкремент) -- (дикремент) нужно писать до или после, т.к. они добавляют/убавляют единицу
        int var = 10;
        var = ++var;
        System.out.println(var);

        // 2. Сравнения < > = <= >= а равно это два символа ==, а != это не равно

        // 3. Логические &&(&) и ||(|) или ! отрицание

        String name = "Oleg";
        int age = 31;
        System.out.println(name.equals("Oleg") && age == 31);

        // 4. Присвоения = += -= *=

        // 5. Оператор instenceof - возвращает true если обьект соответствует нужному типу

        //boolean ageTypeTrue = age instanceof int;

        // 6. Тернарный оператор ? это if : это else
        char sex = 'm';
        String nameFirst = sex == 'm'
                ? "Valera"
                : "Maria";
//оба абзаца дублируют друг друга. Можно писать и так, и так.

        if (sex == 'm') {
            nameFirst = "Valera";
        } else {
            nameFirst = "Matia";
        }

    }
}


