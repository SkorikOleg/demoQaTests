// voidDD - означает, что значение не возвращается. Используется в обозначении метода
//System - класс
// out - это обьект, который находится в классе System
// println - это метод внутри области видимости
// ("Hello World!") то, что внутри скобок - это строковый литерал
// String (тип данных) statement (имя переменной) = "Hello World!" (значение) - это обьявление переменной

